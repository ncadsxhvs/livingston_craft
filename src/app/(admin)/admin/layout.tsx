export default async function AdminLayout({
  children,
}: {
  children: React.ReactNode;
}) {
  const supabase = await createClient();
  const {
    data: { user },
  } = await supabase.auth.getUser();

  if (!user) {
    redirect("/login");
  }

  return (
    <div className="min-h-screen bg-cream-50">
      <aside className="fixed left-0 top-0 h-full w-60 border-r border-stone-200 bg-white">
        <div className="px-6 py-6">
          <h2 className="font-serif text-xl font-light text-wood-900">
            Livingston Craft
          </h2>
          <p className="mt-0.5 text-xs text-stone-400">Admin</p>
        </div>
        <nav className="px-3">
          {navItems.map((item) => (
            <Link
              key={item.href}
              href={item.href}
              className="flex items-center gap-3 rounded-lg px-3 py-2.5 text-sm font-medium text-stone-600 transition-colors hover:bg-cream-50 hover:text-wood-900"
            >
              <NavIcon icon={item.icon} />
              {item.label}
            </Link>
          ))}
        </nav>
        <div className="absolute bottom-0 left-0 w-full border-t border-stone-200 p-4">
          <p className="truncate text-xs text-stone-400">{user.email}</p>
        </div>
      </aside>

      <main className="ml-60 p-8">{children}</main>
    </div>
  );
}
